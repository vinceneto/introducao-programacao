#include <stdio.h> // BIBLIOTECA PADRÃO

int main()
{
    //Caracteres
    char caractere = 'A';

    //Inteiros
    int inteiro = 33;

    //Ponto flutuante
    float ponto_flutuante = 3.14159;

    //Ponto flutuante duuplo
    double duplo = 2.718281828;

    //Impressão usando printf
    printf("Caracter: %c\n", caractere);
    printf("Inteiro: %d\n", inteiro);
    printf("Ponto flutuante: %f\n", ponto_flutuante);
    printf("Duplo: %lf", duplo);

    return 0;
}
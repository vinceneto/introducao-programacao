#include <stdio.h>

int main()
{
    int i = 0;      // Express達o Inicial
    int j = 10;

    while( i < 10){    // Express達o Booleana
        printf("%d ", i);
        i++;        // Express達o de Incremento
    }
    //printf("\n\n>>VALOR FINAL DO CONTADOR: %d<<\n", i);
    printf("\n");

    while (j >= 1){
        printf("%d ", j);
        j--;        // Express達o de Decremento
    }
    
    return 0;
}